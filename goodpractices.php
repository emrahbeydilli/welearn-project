          <div class="card-body">
            <p><strong>Contrast American Method: </strong>Developed by Stewart et al. (1966), it consists of videotaping intercultural interactions which are proposed to students in class. Students are requested to analyze behavioral and values differences, and to suggest how to interact more effective. For more info:</p>
            <ul class="list-group list-group-horizontal">
              <li class="list-group-item "><a href="https://files.eric.ed.gov/fulltext/EJ1083384.pdf " target="_blank">Read the Document</a></li>
            </ul>
            <hr>
            <p><strong>Crossing Borders:</strong> This is a 56-minutes documentary created by Arnd Wächter in 2016 which show a short study abroad experience. Characters are students involved in the experience and that participated to the experience knowing that they would be video recorded during the all two weeks abroad intensive experience. It shows students expectation, their challenges and intercultural learning. For more info:</p>
            <ul class="list-group list-group-horizontal">
              <li class="list-group-item "><a href="https://crossingborders.education/films/crossi" target="_blank">Visit the Website</a></li>
            </ul>
            <hr>
            <p><strong>A World of difference:</strong> working successfully across cultures. This is a 43-minutes didactical movie created by WorldWork. It reproduces a critical incident situation of an international team of managers who are getting and trying to work together for the first time for launching a new corporate’s project. It is based on cross-cultural dimensions model and on International Profile Index to assess and develop intercultural skills. It includes teaching notes to help facilitators to use this video as didactical tool. For more info:</p>
            <ul class="list-group list-group-horizontal">
              <li class="list-group-item "><a href="https://worldwork.global/world-difference-wod/" target="_blank">Visit the Website</a></li>
            </ul>
            <hr>
            <p><strong>The New Math of Multicultural Workgroups:</strong> Video produced by JAMK University during an intercultural communication course involving students to write the script, as actors and in the video-editing. The video reproduces a critical incident involving teacher and international students in a multicultural teamworking project. Teaching notes to for facilitators and teaching notes are also included. For more info: </p>
            <ul class="list-group list-group-horizontal">
              <li class="list-group-item "><a href="https://www.jamk.fi/en/Education/global-education-services/Multicultural-workgroups/" target="_blank">Visit the Website</a></li>
            </ul>
            <hr>
            <p><strong>The Case for Global Leadership: the Kai Bendix story.</strong> It is a 43-minutes didactical movie created by WorldWork. It reproduces a cricital incident situation of a German manager expatriating in India, after a previous international experience in Bulgaria, who has to face a local branch problem regarding bribery. It shows a cultural perspective about bribery and managing people across countries, and a Global Sustainable Leadership model. For more info:</p>
            <ul class="list-group list-group-horizontal">
              <li class="list-group-item "><a href="https://worldwork.global/kai-bendix-video-kai/" target="_blank">Visit the Website</a></li>
            </ul>
            <hr>
