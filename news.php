                    <p>Neighbourness refers to the ability of living and learning together, and represents today an urgent need, due to the increase of human mobility on one hand, and of the technological hyper connectivity on the other hand. Neighbourness is an important set of competences in present disruptive, fluid and complex times, where citizens (and particularly younger generations) increasingly look at global horizons, while being at risk of losing interest for local roots and sense of belonging.</p>
                    <p>The internationalization of higher education, linked to the increased freedom of students’ and teachers’ movement, plays here an important role. And universities are therefore called to design and adopt tools that help academics to promote the development of neighbourness competences, through innovative intercultural pedagogy practices and tools, accompanied by adequate university’s internationalization strategies and practices.</p>
